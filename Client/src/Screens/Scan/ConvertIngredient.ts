const ingredientsArrayEnglish = ingredientsEnglish.trim().split('\n');
const ingredientsArrayVietnamese = ingredientsVietNam.trim().split('\n');


const ingredients:any = {};

for (let i = 0; i < ingredientsArrayEnglish.length; i++) {
    ingredients[ingredientsArrayEnglish[i]] = ingredientsArrayVietnamese[i];
}

export const convertIngredient = (list:string[])=>{
    let results=[];
  
    for(let ele of list){

        for (let i = 0; i < ingredientsArrayEnglish.length; i ++)
        {
          
            if (ele == ingredientsArrayEnglish[i]) results.push(ingredients[ele])
        }
    }
    return results.join(', ');
}